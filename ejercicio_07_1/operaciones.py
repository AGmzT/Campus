# modulo con las operaciones basicas de una calculadora

class operaciones():
    
    def sumar(self, a , b):
        
        """devuelve la suma de a + b"""
        
        return a + b
    
    def restar(self, a , b):
        
        """devuelve la suma de a - b"""
        
        return a - b
    
    def multip(self, a , b):
        
        """devuelve la multiplicación de a por b"""
        
        return a * b
    
    def div(self, a , b):
        
        """devuelve la divición de a entre b"""
        
        return a / b



